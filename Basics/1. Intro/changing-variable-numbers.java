// Program to change the value of a variable.


class Main {
    public static void main(String[] args) {
     
        // create and print the number variable
        double number = 32.54;
        System.out.println(number);
     
        // change the value of number to 87.62
        number = 87.62;
      
        // print the number variable
        System.out.println(number);
     }
}