// Program to implement a double type array.


class Main {
    public static void main(String[] args) {
        
        // create double array
        double[] numbers = {3.5, 8.9, 1.6, -4.8};
        
        // access array elements using the for loop
        for (int i = 0; i < 4; i++) {
            System.out.println(numbers[i]);
        }
    }
}