// Program to print numbers from 5 to 1 using a for loop.


class Main {

    public static void main(String[] args) {
        // run a for loop from i = 5 to 1
        // print the value of i
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }
    }
}


