// Program to print numbers using a method.


class Main {

    // create printNumbers method
    void printNumbers() {
        // print 5
        System.out.println(5);
        // print 100
        System.out.println(100);
  }

    public static void main(String[] args) {

        // creating object of Main class
        Main obj = new Main();
    
        // call printNumbers()
        obj.printNumbers();
    
        // call printNumbers() again
        obj.printNumbers();
    }
}